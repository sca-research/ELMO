static uint8_t getSBoxValue(uint8_t num)
{
    return sbox[num];
}

//key expansion
void KeyExpansion(uint8_t* Key)
{
  uint32_t i, j, k;
  uint8_t tempa[4]; 
  
  // The first round key
  for(i = 0; i < Nk; ++i)
  {
    roundKey[(i * 4) + 0] = Key[(i * 4) + 0];
    roundKey[(i * 4) + 1] = Key[(i * 4) + 1];
    roundKey[(i * 4) + 2] = Key[(i * 4) + 2];
    roundKey[(i * 4) + 3] = Key[(i * 4) + 3];
  }

  // all the key for other key rounds
  for(; (i < (Nb * (Nr + 1))); ++i)
  {
    for(j = 0; j < 4; ++j)
    {
      tempa[j]=roundKey[(i-1) * 4 + j];
    }

    if (i % Nk == 0)
    {
      {
        k = tempa[0];
        tempa[0] = tempa[1];
        tempa[1] = tempa[2];
        tempa[2] = tempa[3];
        tempa[3] = k;
      }

      {
        tempa[0] = getSBoxValue(tempa[0]);
        tempa[1] = getSBoxValue(tempa[1]);
        tempa[2] = getSBoxValue(tempa[2]);
        tempa[3] = getSBoxValue(tempa[3]);
      }

      tempa[0] =  tempa[0] ^ Rcon[i/Nk];
    }
    else if (Nk > 6 && i % Nk == 4)
    {
      {
        tempa[0] = getSBoxValue(tempa[0]);
        tempa[1] = getSBoxValue(tempa[1]);
        tempa[2] = getSBoxValue(tempa[2]);
        tempa[3] = getSBoxValue(tempa[3]);
      }
    }
    roundKey[i * 4 + 0] = roundKey[(i - Nk) * 4 + 0] ^ tempa[0];
    roundKey[i * 4 + 1] = roundKey[(i - Nk) * 4 + 1] ^ tempa[1];
    roundKey[i * 4 + 2] = roundKey[(i - Nk) * 4 + 2] ^ tempa[2];
    roundKey[i * 4 + 3] = roundKey[(i - Nk) * 4 + 3] ^ tempa[3];
  }

}


void mixColumns(uint8_t state[16]){

 /* Normal Mix Columns:
 * [w]      [2  3  1  1]    [a]
 * [x]  =   [1  2  3  1]  * [b]
 * [y]      [1  1  2  3]    [c]
 * [z]      [3  1  1  2]    [d]
 * 
 */

    uint8_t temp[4];
    uint8_t i;
    for(i = 0; i < 16; i+=4){
        temp[0] = state[i];
        temp[1] = state[i+1];
        temp[2] = state[i+2];
        temp[3] = state[i+3];

        state[i]	=	mul_02[temp[0]]	^ mul_03[temp[1]]   ^       temp[2]	    ^       temp[3];
        state[i+1]	=	       temp[0]	^ mul_02[temp[1]]	^mul_03[temp[2]]	^       temp[3];
        state[i+2]	=	       temp[0]	^        temp[1]	^mul_02[temp[2]]	^mul_03[temp[3]];
        state[i+3]	=	mul_03[temp[0]]	^        temp[1]	^       temp[2]	    ^mul_02[temp[3]];
    }
}
